  static String fechaModficiada(String fecha) {
    var fecha_hora = fecha.split(" ");
    var fechapart = fecha_hora[0].split("-");
    var horapart = fecha_hora[1].split(":");

    String month = "";
    switch (fechapart[1]) {
      case "01":
        month = "Enero";
        break;
      case "02":
        month = "Febrero";
        break;
      case "03":
        month = "Marzo";
        break;
      case "04":
        month = "Abril";
        break;
      case "05":
        month = "Mayo";
        break;
      case "06":
        month = "Junio";
        break;
      case "07":
        month = "Julio";
        break;
      case "08":
        month = "Agosto";
        break;
      case "09":
        month = "Septiembre";
        break;
      case "10":
        month = "Octubre";
        break;
      case "11":
        month = "Noviembre";
        break;
      case "12":
        month = "Diciembre";
        break;
    }

    return fechapart[2] +
        " de " +
        month +
        " del " +
        fechapart[0] +
        "_" +
        horapart[0] +
        ":" +
        horapart[1];
  }

